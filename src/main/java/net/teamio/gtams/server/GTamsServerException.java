package net.teamio.gtams.server;

public class GTamsServerException extends Exception {

	public GTamsServerException() {
	}

	public GTamsServerException(String arg0) {
		super(arg0);
	}

	public GTamsServerException(Throwable arg0) {
		super(arg0);
	}

	public GTamsServerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GTamsServerException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
